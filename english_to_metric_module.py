# Length Conversions
def inch_to_cm(x):
    return x * 2.54

def foot_to_cm(x):
    return x * 30.48

def yard_to_meter(x):
    return x * 0.9144

def foot_to_meter(x):
    return x * 0.3048

def miles_to_kilometer(x):
    return x * 1.60934

# Mass/Weight Conversions
def ounce_to_grams(x):
    return x * 28.3495

def pound_to_kilogram(x):
    return x * 0.453592

# Volume Conversions
def gallon_to_liter(x):
    return x * 3.78541

def quart_to_liter(x):
    return x * 0.946353

def pint_to_liter(x):
    return x * 0.473176

def cup_to_milliliter(x):
    return x * 236.588

# Area Conversions
def square_inch_to_square_cm(x):
    return x * 6.4516

def square_foot_to_square_meter(x):
    return x * 0.092903

def acre_to_hectare(x):
    return x * 0.404686

